s='HELLO world HOW are you'
s1=s.capitalize()#first letter uppercase of the string
s2=s.upper()#all letters uppercase of the string
s3=s.lower()#all letters lowercase of the string
s4=s.title()#first letter uppercase of each word in the string
s5=s.swapcase()#this chaneg all the uppercase letters to lowercase and lowercase letters to uppercase
s6=s.casefold()#this is similar to lower() but more aggressive in converting string to lowercase    
print(s1)
print(s2)
print(s3)
print(s4)
print(s5)
print(s6)